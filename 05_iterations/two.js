// +++++++++++++++++++++++++ while loop +++++++++++++++++++++++++++++++++++++ // 



let index = 0
while (index <= 10) {
    // console.log(`value of index is ${index}`); 
    index = index + 2
}

let myArr = ["flash", "spiderman", "batman", "superman"]

let count = 0
while (count < myArr.length) {
    // console.log(`Value is ${myArr[count]}`); 
    count = count + 1
}


// ++++++++++++++++++++++++ do while loop +++++++++++++++++++++++++++++++++++++++ //

let score = 11

do {
    console.log(`Score is ${score}`);
    score ++
} while (score <= 10);

